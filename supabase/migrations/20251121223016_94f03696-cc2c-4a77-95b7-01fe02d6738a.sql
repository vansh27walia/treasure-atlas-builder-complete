-- Fix search_path security issue for trigger functions
-- This prevents potential privilege escalation attacks

-- Update trigger function for bulk_shipments table
CREATE OR REPLACE FUNCTION public.update_bulk_shipments_updated_at()
RETURNS trigger
LANGUAGE plpgsql
SET search_path = public
AS $function$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$function$;

